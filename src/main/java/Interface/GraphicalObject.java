package Interface;

import java.awt.image.BufferedImage;

/**
 * The interface Graphical object.
 */
public interface GraphicalObject {
    /**
     * Gets image.
     *
     * @return the image
     */
    BufferedImage getImage();

    /**
     * Sets image.
     *
     * @param image the image
     */
    void setImage(BufferedImage image);
}
