package Interface;

/**
 * The interface Vector.
 */
public interface Vector {
    /**
     * Gets x.
     *
     * @return the x
     */
    int getX();

    /**
     * Gets y.
     *
     * @return the y
     */
    int getY();

    /**
     * Sets x.
     *
     * @param x the x
     */
    void setX(int x);

    /**
     * Sets y.
     *
     * @param y the y
     */
    void setY(int y);
}
